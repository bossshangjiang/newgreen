// 改变用户级别
function uplevel1() {
    var data = 'number=' + $('#n3').text();
    $.ajax({
        type: "POST",
        data: data,
        url: "/uplevel",
        success: function (data) {
            if (data="success") {
                alert("操作成功");
            }
            else
                alert("操作失败");
        }
    });

}

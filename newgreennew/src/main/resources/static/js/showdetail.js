
function showdetailx1() {
    var data = 'pname=' + $('#m3').text();
    alert(data);
    $.ajax({
        type: "POST",
        data:data,
        url: "/detail",
        success: function (data) {
            if(data!="000")
                window.location.href ='/detail.html?pname='+data;
            alert("成功查看详情");
        }


    });
}


// 用于删除用户
function delete1() {
    var data = 'number=' + $('#n3').text();
    $.ajax({
        type: "POST",
        data: data,
        url: "/deleteuser",
        success: function (data) {
            if (data="success") {
                alert("操作成功");
            }
            else
                alert("操作失败");
        }
    });

}
